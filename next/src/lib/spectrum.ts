/**
 * Shuffles a new deck of spectrum cards for the game.
 *
 * @returns The shuffled deck of spectrum cards.
 */

/**
 * Generates a random spectrum target value between 0 and 100.
 *
 * @returns The generated spectrum target value.
 */
export const generateSpectrumTarget = () => {
  return Math.floor(Math.random() * 100);
};
